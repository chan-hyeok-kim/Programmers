SELECT DISTINCT B.AUTHOR_ID,AUTHOR_NAME,CATEGORY,AVG(PRICE)*SUM(SALES) AS TOTAL_SALES
  FROM AUTHOR A INNER JOIN BOOK B
    ON A.AUTHOR_ID=B.AUTHOR_ID INNER JOIN BOOK_SALES BS
    ON B.BOOK_ID=BS.BOOK_ID
 WHERE TO_CHAR(SALES_DATE,'YYYY-MM')='2022-01'
 GROUP BY B.AUTHOR_ID,AUTHOR_NAME,CATEGORY
 ORDER BY B.AUTHOR_ID, CATEGORY DESC;
