SELECT U.USER_ID, U.NICKNAME, U.AMOUNT AS TOTAL_SALES
  FROM 
(
    SELECT UGU.USER_ID, UGU.NICKNAME, SUM(UGB.PRICE) AS AMOUNT
      FROM USED_GOODS_BOARD UGB INNER JOIN USED_GOODS_USER UGU
        ON UGB.WRITER_ID=UGU.USER_ID
     WHERE STATUS='DONE'
     GROUP BY UGU.USER_ID,UGU.NICKNAME
    HAVING SUM(UGB.PRICE)>=700000
) U   
ORDER BY U.AMOUNT;
